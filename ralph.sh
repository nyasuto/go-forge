#!/bin/bash

# 無限ループやAPI課金の暴走を防ぐための上限回数
MAX_LOOPS=10

for ((i=1; i<=MAX_LOOPS; i++)); do
    # 全タスク完了チェック: prd.mdに未完了タスク [ ] が残っていなければ終了
    if ! grep -q '\- \[ \]' prd.md 2>/dev/null; then
        echo "========================================"
        echo " 全タスク完了！ループを終了します。"
        echo "========================================"
        exit 0
    fi

    echo "========================================"
    echo " Ralph Loop $i / $MAX_LOOPS 回目を開始します..."
    echo "========================================"

    claude --dangerously-skip-permissions -p "$(cat prompt.md)"

    echo "========================================"
    echo " Loop $i 終了（コンテキストをリセット）"
    echo " 次のループまで5秒待機します..."
    echo "========================================"
    sleep 5
done

echo "指定された最大ループ回数 ($MAX_LOOPS 回) に到達しました。"